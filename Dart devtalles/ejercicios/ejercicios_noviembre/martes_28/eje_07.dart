/* 4. Estructuras de Control de Flujo
Ejercicio 7: Crea un programa que determine 
si un número es par o impar. Usa if para verificar la condición 
e imprime un mensaje adecuado.

  */
main() {
  esParONo(x) {
    if (x % 2 == 0) {
      print('Es un número par');
    } else {
      print('es un número impar');
    }
  }

  esParONo(53543);
}
